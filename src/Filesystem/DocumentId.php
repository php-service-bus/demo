declare(strict_types = 1);

namespace App\Filesystem;

/**
 *
 */
final class DocumentId
{
    /**
     * @var string
     */
    private $value;

    public function __construct(string $value)
    {
        $this->value = $value;
    }

    public function toString(): string
    {
        return $this->value;
    }
}
